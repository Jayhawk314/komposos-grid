@dataclass(frozen=True)
class Quantale:
    """
    A complete lattice with an associative tensor product and unit.
    Determines how confidence scores compose and what 'better' means.
    """
    name: str
    tensor:  Callable[[float, float], float]
    unit:    float
    leq:     Callable[[float, float], bool]   # a <= b in the quantale order

    def compose(self, a: float, b: float) -> float:
        return self.tensor(a, b)

    def compose_path(self, weights: list[float]) -> float:
        result = self.unit
        for w in weights:
            result = self.tensor(result, w)
        return result

    def better(self, new: float, old: float) -> bool:
        """Is new strictly better than old?"""
        return self.leq(old, new) and not math.isclose(new, old, rel_tol=1e-9)

    def at_least_as_good(self, new: float, old: float) -> bool:
        """Tarski condition: new >= old."""
        return self.leq(old, new)

    def loss(self, w: float) -> float:
        """Treat weight as negative log-loss for display purposes."""
        if w <= 0:
            return float('inf')
        return -math.log(w)


MULTIPLICATIVE = Quantale(
    name="multiplicative",
    tensor=lambda a, b: a * b,
    unit=1.0,
    leq=lambda a, b: a <= b         # higher confidence = better
)

ADDITIVE = Quantale(
    name="additive",
    tensor=lambda a, b: a + b,
    unit=0.0,
    leq=lambda a, b: a >= b         # lower cost = better
)

MIN_QUANTALE = Quantale(
    name="min",
    tensor=min,
    unit=1.0,
    leq=lambda a, b: a <= b         # bottleneck: best = highest minimum link
)


