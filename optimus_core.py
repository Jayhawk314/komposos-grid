# ENRICHED RUNTIME CATEGORY
# ══════════════════════════════════════════════════════════════════════════════

class RuntimeCategory:
    """
    The enriched runtime category.

    Every morphism carries a quantale-valued weight.
    Composition propagates weights via the quantale tensor.
    Maintains the complete morphism graph for factorization search.
    """

    def __init__(self, name: str = "RuntimeCat",
                 quantale: Quantale = MULTIPLICATIVE):
        self.name = name
        self.quantale = quantale
        self.objects:   dict[str, dict] = {}
        self.morphisms: dict[str, EnrichedMorphism] = {}
        self._history:  list[dict] = []           # audit trail

    # ── Objects ──────────────────────────────────────────────────

    def add_object(self, name: str, **attrs) -> str:
        self.objects[name] = attrs
        return name

    def add_objects(self, *names: str) -> list[str]:
        for n in names:
            self.add_object(n)
        return list(names)

    def get_or_create_object(self, name: str) -> str:
        if name not in self.objects:
            self.add_object(name)
        return name

    # ── Morphisms ─────────────────────────────────────────────────

    def add_morphism(self, name: str, src: str, tgt: str,
                     confidence: float = 1.0,
                     fn: Optional[Callable] = None,
                     **metadata) -> EnrichedMorphism:
        self.get_or_create_object(src)
        self.get_or_create_object(tgt)
        m = EnrichedMorphism(name, src, tgt, confidence, fn,
                             provenance=[name], metadata=metadata)
        self.morphisms[name] = m
        self._history.append({"op": "add", "morphism": name,
                               "confidence": confidence})
        return m

    def identity(self, obj: str) -> EnrichedMorphism:
        name = f"id({obj})"
        if name in self.morphisms:
            return self.morphisms[name]
        m = EnrichedMorphism(name, obj, obj, self.quantale.unit,
                             fn=lambda x: x, provenance=[])
        self.morphisms[name] = m
        return m

    def compose(self, f: EnrichedMorphism,
                g: EnrichedMorphism,
                name: Optional[str] = None,
                store: bool = True) -> EnrichedMorphism:
        """
        Compose f then g.
        confidence(g∘f) = quantale.tensor(f.confidence, g.confidence)
        """
        if f.target != g.source:
            raise ValueError(
                f"Not composable: {f.name}.target={f.target} ≠ "
                f"{g.name}.source={g.source}"
            )
        confidence = self.quantale.compose(f.confidence, g.confidence)
        composed_fn = None
        if f.fn and g.fn:
            composed_fn = lambda x, _f=f.fn, _g=g.fn: _g(_f(x))
        name = name or f"{g.name}∘{f.name}"
        generation = max(f.generation, g.generation)
        m = EnrichedMorphism(
            name, f.source, g.target, confidence, composed_fn,
            f.provenance + g.provenance, generation
        )
        if store:
            self.morphisms[name] = m
            self._history.append({"op": "compose", "morphism": name,
                                   "confidence": confidence})
        return m

    def compose_path(self, morphisms: list[EnrichedMorphism],
                     name: Optional[str] = None) -> EnrichedMorphism:
        """Compose a sequence of morphisms left-to-right."""
        assert morphisms, "Empty path"
        result = morphisms[0]
        for m in morphisms[1:]:
            result = self.compose(result, m, store=False)
        if name:
            result.name = name
        self.morphisms[result.name] = result
        return result

    # ── Graph queries ─────────────────────────────────────────────

    def morphisms_from(self, src: str) -> list[EnrichedMorphism]:
        return [m for m in self.morphisms.values() if m.source == src]

    def morphisms_to(self, tgt: str) -> list[EnrichedMorphism]:
        return [m for m in self.morphisms.values() if m.target == tgt]

    def morphisms_between(self, src: str,
                          tgt: str) -> list[EnrichedMorphism]:
        return [m for m in self.morphisms.values()
                if m.source == src and m.target == tgt]

    def best_morphism(self, src: str,
                      tgt: str) -> Optional[EnrichedMorphism]:
        candidates = self.morphisms_between(src, tgt)
        if not candidates:
            return None
        return max(candidates, key=lambda m: m.confidence
                   if self.quantale.name == "multiplicative" else -m.confidence)

    # ── Yoneda representation ──────────────────────────────────────

    def yoneda_in(self, obj: str) -> dict[str, EnrichedMorphism]:
        """Hom(-, obj): all morphisms into obj. The contravariant Yoneda."""
        return {m.name: m for m in self.morphisms_to(obj)}

    def yoneda_out(self, obj: str) -> dict[str, EnrichedMorphism]:
        """Hom(obj, -): all morphisms out of obj. The covariant Yoneda."""
        return {m.name: m for m in self.morphisms_from(obj)}

    def yoneda_fingerprint(self, obj: str) -> dict:
        """
        Full relational fingerprint of an object.
        Two objects with identical fingerprints are isomorphic.
        """
        return {
            "object": obj,
            "hom_in":  {k: v.confidence for k, v in self.yoneda_in(obj).items()},
            "hom_out": {k: v.confidence for k, v in self.yoneda_out(obj).items()},
        }

    def yoneda_similarity(self, a: str, b: str) -> float:
        """
        Cosine-like similarity between two objects' Yoneda fingerprints.
        1.0 = structurally identical, 0.0 = no shared relationships.
        """
        fp_a = self.yoneda_fingerprint(a)
        fp_b = self.yoneda_fingerprint(b)

        all_keys = (set(fp_a["hom_in"]) | set(fp_b["hom_in"]) |
                    set(fp_a["hom_out"]) | set(fp_b["hom_out"]))

        if not all_keys:
            return 1.0 if a == b else 0.0

        shared = sum(
            min(fp_a["hom_in"].get(k, 0), fp_b["hom_in"].get(k, 0)) +
            min(fp_a["hom_out"].get(k, 0), fp_b["hom_out"].get(k, 0))
            for k in all_keys
        )
        total = sum(
            max(fp_a["hom_in"].get(k, 0), fp_b["hom_in"].get(k, 0)) +
            max(fp_a["hom_out"].get(k, 0), fp_b["hom_out"].get(k, 0))
            for k in all_keys
        )
        return shared / total if total > 0 else 0.0

    def stats(self) -> dict:
        return {
            "objects": len(self.objects),
            "morphisms": len(self.morphisms),
            "rewrites": sum(1 for h in self._history if h["op"] == "rewrite"),
        }


