class Functor:
    """
    F : FreeCat(Graph) → RuntimeCat

    The interpreter. Maps syntax to enriched runtime morphisms.
    Functor law: F(g ∘ f) = F(g) ∘ F(f)
    This is enforced structurally: interpreting a path = composing its edges.
    """

    def __init__(self, syntax: FreeCategory, runtime: RuntimeCategory,
                 name: str = "F"):
        self.name = name
        self.syntax = syntax
        self.runtime = runtime
        self._object_map:    dict[str, str] = {}
        self._edge_map:      dict[str, EnrichedMorphism] = {}

    def map_object(self, syn_obj: str,
                   run_obj: Optional[str] = None) -> str:
        run_obj = run_obj or syn_obj
        self.runtime.get_or_create_object(run_obj)
        self._object_map[syn_obj] = run_obj
        return run_obj

    def map_edge(self, edge_name: str,
                 morphism: EnrichedMorphism) -> EnrichedMorphism:
        self._edge_map[edge_name] = morphism
        return morphism

    def interpret(self, path: Path) -> EnrichedMorphism:
        """
        Interpret a free-category path as an enriched runtime morphism.
        F(g ∘ f) = F(g) ∘ F(f)  — functor law holds by construction.
        """
        if path.is_identity:
            obj = self._object_map.get(path.source, path.source)
            return self.runtime.identity(obj)

        morphisms = []
        for edge in path.edges:
            m = self._edge_map.get(edge)
            if m is None:
                raise KeyError(
                    f"Functor {self.name}: no mapping for edge '{edge}'"
                )
            morphisms.append(m)

        return self.runtime.compose_path(morphisms)

    def verify_functor_law(self, f_name: str, g_name: str) -> bool:
        """
        Check F(g ∘ f) == F(g) ∘ F(f) for named edges f, g.
        Returns True if functor law holds.
        """
        syn = self.syntax
        f_path = Path(*syn.edges[f_name], [f_name])
        g_path = Path(*syn.edges[g_name], [g_name])

        if f_path.target != g_path.source:
            return True    # not composable, vacuously true

        composed_path = syn.compose(f_path, g_path)

        F_gof = self.interpret(composed_path)
        F_f   = self.interpret(f_path)
        F_g   = self.interpret(g_path)
        F_g_after_F_f = self.runtime.compose(F_f, F_g, store=False)

        return (F_gof.source == F_g_after_F_f.source and
                F_gof.target == F_g_after_F_f.target and
                math.isclose(F_gof.confidence,
                             F_g_after_F_f.confidence, rel_tol=1e-9))


