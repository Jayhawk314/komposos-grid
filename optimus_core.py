@dataclass
class Rewrite:
    """A single rewrite event produced by the Optimus monad."""
    kind:               str              # 'compress' | 'factorize' | 'absorb'
    old_morphisms:      list[str]
    new_morphism:       str
    confidence_before:  list[float]
    confidence_after:   float
    generation:         int
    timestamp:          float = field(default_factory=time.time)

    def improvement(self, q: Quantale) -> float:
        """How much better is the new morphism vs the composed old path?"""
        old_conf = q.compose_path(self.confidence_before)
        return self.confidence_after - old_conf

    def __repr__(self):
        before = " ⊗ ".join(f"{c:.3f}" for c in self.confidence_before)
        arrow = " → ".join(self.old_morphisms)
        return (f"[gen={self.generation}] {arrow} "
                f"⟶ {self.new_morphism}  "
                f"[{before} = {self.confidence_after:.4f}]")


class OptimisMonad:
    """
    Optimus : Monad on RuntimeCat

    The refinement engine. Searches for better factorizations of morphisms.

    Monad structure:
        unit     : m → {m}                    lift, no change
        multiply : {{m}} → {m}                collapse nested search
        bind     : m → (m → {n}) → {n}        chain refinements

    The categorical gradient:
        Given m : A → C, search all objects B such that A→B and B→C exist.
        Score each factorization A→B→C by w(A→B) ⊗ w(B→C).
        Return the best factorization if it beats the current morphism.
        This is hill-climbing in morphism space.

    Tarski stability:
        Every accepted rewrite satisfies w(new) >= w(old).
        The system is monotone and converges to a fixpoint.
    """

    def __init__(self, runtime: RuntimeCategory, max_depth: int = 3):
        self.runtime   = runtime
        self.max_depth = max_depth
        self.rewrites:  list[Rewrite] = []
        self.generation = 0

    # ── Monad unit ────────────────────────────────────────────────

    def unit(self, m: EnrichedMorphism) -> list[EnrichedMorphism]:
        """Lift a morphism into the monad. Identity action."""
        return [m]

    # ── Monad multiply ────────────────────────────────────────────

    def multiply(self, candidates: list[list[EnrichedMorphism]]
                 ) -> list[EnrichedMorphism]:
        """
        Monad multiplication: collapse nested candidate sets.
        Optimus²(m) → Optimus(m)
        Prevents exponential explosion of nested search.
        """
        flat = {}
        for group in candidates:
            for m in group:
                key = (m.source, m.target)
                existing = flat.get(key)
                if existing is None or self.runtime.quantale.better(
                        m.confidence, existing.confidence):
                    flat[key] = m
        return list(flat.values())

    # ── Monad bind ────────────────────────────────────────────────

    def bind(self, m: EnrichedMorphism,
             f: Callable[[EnrichedMorphism],
                         list[EnrichedMorphism]]) -> list[EnrichedMorphism]:
        """Chain refinements: m >>= f"""
        return self.multiply([f(n) for n in self.unit(m)])

    # ── Factorization search  (the categorical gradient) ──────────

    def factorizations(self, m: EnrichedMorphism,
                       depth: int = 1) -> list[list[EnrichedMorphism]]:
        """
        Find all factorizations of m : A → C.

        depth=1: single intermediate  A → B → C
        depth=2: two intermediates    A → B → D → C
        etc.

        Returns a list of paths (each path = list of morphisms).
        Only returns paths with strictly better confidence than m.
        """
        q = self.runtime.quantale
        results = []
        self._factorize_rec(
            m.source, m.target, [], m.confidence, depth, results
        )
        return results

    def _factorize_rec(self, current: str, target: str,
                       path_so_far: list[EnrichedMorphism],
                       threshold: float,
                       depth: int,
                       results: list) -> None:
        """
        DFS over factorizations.
        depth = additional intermediates allowed.
        At each node: try completing to target, then recurse deeper.
        """
        q = self.runtime.quantale

        # Try to complete the path directly to target (requires >=1 edge already)
        if len(path_so_far) > 0:
            for m in self.runtime.morphisms_from(current):
                if m.target == target:
                    full_path = path_so_far + [m]
                    conf = q.compose_path([x.confidence for x in full_path])
                    if q.better(conf, threshold):
                        results.append(full_path)

        if depth == 0:
            return

        # Explore further intermediates
        for m in self.runtime.morphisms_from(current):
            if m.target == target:
                continue  # already handled above
            self._factorize_rec(
                m.target, target,
                path_so_far + [m],
                threshold, depth - 1, results
            )

    # ── Compression  (materialize a path as a single morphism) ────

    def compress(self, path: list[EnrichedMorphism],
                 name: Optional[str] = None) -> Optional[EnrichedMorphism]:
        """
        Materialize a multi-hop path as a single shortcut morphism.
        Only accepted if it improves over any existing A→Z morphism.
        Tarski condition: new confidence >= existing best confidence.
        """
        assert len(path) >= 2, "Need at least 2 morphisms to compress"
        q = self.runtime.quantale

        confidence = q.compose_path([m.confidence for m in path])
        existing = self.runtime.best_morphism(path[0].source, path[-1].target)

        if existing and not q.better(confidence, existing.confidence):
            return None    # no improvement — reject

        name = name or f"shortcut({path[0].source}→{path[-1].target})"
        composed_fn = None
        fns = [m.fn for m in path]
        if all(fns):
            def make_pipeline(funcs):
                def pipeline(x):
                    for f in funcs:
                        x = f(x)
                    return x
                return pipeline
            composed_fn = make_pipeline(fns)

        shortcut = EnrichedMorphism(
            name, path[0].source, path[-1].target, confidence,
            composed_fn,
            provenance=[e for m in path for e in m.provenance],
            generation=self.generation
        )
        self.runtime.morphisms[name] = shortcut

        rewrite = Rewrite(
            kind="compress",
            old_morphisms=[m.name for m in path],
            new_morphism=name,
            confidence_before=[m.confidence for m in path],
            confidence_after=confidence,
            generation=self.generation
        )
        self.rewrites.append(rewrite)
        self.runtime._history.append({"op": "rewrite", "morphism": name,
                                       "confidence": confidence})
        return shortcut

    # ── Refine a single morphism  (one gradient step) ─────────────

    def refine(self, m: EnrichedMorphism,
               depth: int = 1) -> Optional[EnrichedMorphism]:
        """
        One Optimus step on a morphism m.

        1. Generate candidate factorizations (the 'gradient direction').
        2. Score each by quantale composition.
        3. Accept the best if it improves m (Tarski condition).
        4. Materialize as a shortcut morphism.

        Returns the improved morphism, or None if no improvement found.
        """
        candidates = self.factorizations(m, depth=depth)
        if not candidates:
            return None

        q = self.runtime.quantale
        best_path = max(
            candidates,
            key=lambda path: q.compose_path([x.confidence for x in path])
        )
        best_conf = q.compose_path([x.confidence for x in best_path])

        if not q.better(best_conf, m.confidence):
            return None

        return self.compress(best_path)

    # ── Descent loop  (iterate until fixpoint) ────────────────────

    def descend(self, morphisms: Optional[list[str]] = None,
                max_steps: int = 20,
                depth: int = 1,
                verbose: bool = True) -> dict:
        """
        Categorical gradient descent loop.

        For each morphism in scope:
            1. Search for a better factorization.
            2. If found, accept it and add it to scope.
            3. Repeat until no morphism can be improved.

        Guaranteed to terminate: each step strictly improves confidence.
        The system converges to a fixpoint (Tarski theorem).

        Returns a summary of improvements made.
        """
        q = self.runtime.quantale
        scope = morphisms or list(self.runtime.morphisms.keys())
        improved = []
        steps = 0

        if verbose:
            print(f"\n── Optimus descent (max_steps={max_steps}, depth={depth})")

        for step in range(max_steps):
            self.generation = step + 1
            found = False

            # Snapshot current scope to avoid mutation during iteration
            current_morphisms = [
                self.runtime.morphisms[name]
                for name in scope
                if name in self.runtime.morphisms
            ]

            for m in current_morphisms:
                result = self.refine(m, depth=depth)
                if result:
                    if verbose:
                        print(f"  step {step+1}: {self.rewrites[-1]}")
                    scope.append(result.name)
                    improved.append(result.name)
                    found = True
                    steps += 1

            if not found:
                if verbose:
                    print(f"  Fixpoint reached after {steps} rewrites.")
                break

        return {
            "steps": steps,
            "improved": improved,
            "rewrites": self.rewrites[-steps:] if steps else [],
        }

    # ── Absorb: Yoneda-guided structural learning ──────────────────

    def absorb(self, source_obj: str, target_obj: str,
               threshold: float = 0.8) -> list[EnrichedMorphism]:
        """
        Kan-extension-inspired generalization.

        Given a bridge morphism source_obj → target_obj (or explicit similarity),
        transfer morphisms from source_obj to target_obj.

        The bridge morphism confidence acts as the similarity weight.
        This is the system discovering that two objects are structurally
        analogous and generalizing across them.
        """
        q = self.runtime.quantale

        # Use bridge morphism confidence as similarity if one exists
        bridge = None
        for m in self.runtime.morphisms_between(source_obj, target_obj):
            if bridge is None or m.confidence > bridge.confidence:
                bridge = m

        if bridge:
            sim = bridge.confidence
        else:
            sim = self.runtime.yoneda_similarity(source_obj, target_obj)

        if sim < threshold:
            return []

        transferred = []
        for m in self.runtime.morphisms_from(source_obj):
            new_name = f"kan({m.name}→{target_obj})"
            if new_name in self.runtime.morphisms:
                continue
            # Transfer with confidence scaled by similarity
            new_conf = q.compose(m.confidence, sim)
            new_m = self.runtime.add_morphism(
                new_name, target_obj, m.target,
                confidence=new_conf,
                fn=m.fn
            )
            new_m.generation = self.generation
            new_m.provenance = [f"kan:{m.name}", f"sim:{sim:.3f}"]
            transferred.append(new_m)

            rewrite = Rewrite(
                kind="absorb",
                old_morphisms=[m.name, f"sim({source_obj},{target_obj})={sim:.3f}"],
                new_morphism=new_name,
                confidence_before=[m.confidence, sim],
                confidence_after=new_conf,
                generation=self.generation
            )
            self.rewrites.append(rewrite)

        return transferred

    # ── Reports ───────────────────────────────────────────────────

    def report(self, verbose: bool = True) -> str:
        lines = [f"\n── Optimus Rewrite Log ({len(self.rewrites)} rewrites) ──"]
        by_kind: dict[str, list] = {}
        for r in self.rewrites:
            by_kind.setdefault(r.kind, []).append(r)

        for kind, rewrites in sorted(by_kind.items()):
            lines.append(f"\n  {kind.upper()} ({len(rewrites)} rewrites)")
            for r in rewrites:
                lines.append(f"    {r}")

        summary = (f"\n  Summary: {len(self.rewrites)} total rewrites | "
                   f"{len(by_kind)} kinds | "
                   f"gen={self.generation}")
        lines.append(summary)
        result = "\n".join(lines)
        if verbose:
            print(result)
        return result
