"""
OPTIMUS — Complete Kernel
=========================

Architecture:
    FreeCat(Graph) --F--> RuntimeCat_Q --Optimus--> RuntimeCat_Q

Three-layer system:
    1. FreeCategory        — syntax, paths as lists, composition = concatenation
    2. RuntimeCategory     — enriched over a quantale, morphisms carry confidence
    3. OptimisMonad        — endofunctor + monad structure + factorization search

The gradient:
    Classical:  x_{t+1} = x_t - η∇L(x_t)
    Optimus:    m_{t+1} = argmax_{f in factorizations(m_t)} w(f)

    Instead of adjusting parameters, Optimus discovers intermediate objects.
    The "gradient direction" is the set of all factorizations A→B→C of a morphism A→C.
    Better factorizations = higher confidence paths = uphill in the quantale order.

Monad structure:
    unit     : m → Optimus(m)              lift (identity action)
    multiply : Optimus²(m) → Optimus(m)   collapse nested search (prevent explosion)
    bind     : m → (m → Optimus(n)) → Optimus(n)   chain refinements

Tarski stability:
    Every rewrite must satisfy w(new) >= w(old) in the quantale order.
    This guarantees monotone convergence to a fixpoint.

Yoneda:
    Every object X is represented by its relational fingerprint:
        hom_in(X)  = {f : A → X for all A}
        hom_out(X) = {f : X → B for all B}
    Two objects with identical fingerprints are categorically indistinguishable.
    Replication = reconstruct from fingerprint.
"""

