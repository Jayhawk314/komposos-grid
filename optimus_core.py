@dataclass
class Path:
    """
    A morphism in the free category.
    Stored as a list of edge names — composition is concatenation.
    Identity morphism = empty list.
    Category laws (associativity, identity) are automatic.
    """
    source: str
    target: str
    edges:  list[str] = field(default_factory=list)

    @property
    def is_identity(self) -> bool:
        return len(self.edges) == 0

    @property
    def length(self) -> int:
        return len(self.edges)

    def __repr__(self):
        if self.is_identity:
            return f"id({self.source})"
        chain = " ∘ ".join(reversed(self.edges))
        return f"[{chain}] : {self.source} → {self.target}"

    def __hash__(self):
        return hash((self.source, self.target, tuple(self.edges)))

    def __eq__(self, other):
        return (self.source == other.source and
                self.target == other.target and
                self.edges == other.edges)


class FreeCategory:
    """
    FreeCat(Graph): the free category generated by a directed graph.

    Objects   = nodes
    Morphisms = finite paths (lists of edges)
    Composition = list concatenation
    Identity  = empty path

    This is the syntax layer. No semantics, no enrichment — pure structure.
    """

    def __init__(self, name: str = "SyntaxCat"):
        self.name = name
        self.objects: set[str] = set()
        self.edges: dict[str, tuple[str, str]] = {}   # name → (src, tgt)

    def add_object(self, name: str) -> str:
        self.objects.add(name)
        return name

    def add_objects(self, *names: str) -> list[str]:
        for n in names:
            self.add_object(n)
        return list(names)

    def add_edge(self, name: str, src: str, tgt: str) -> Path:
        assert src in self.objects, f"Unknown source object: {src}"
        assert tgt in self.objects, f"Unknown target object: {tgt}"
        self.edges[name] = (src, tgt)
        return Path(src, tgt, [name])

    def identity(self, obj: str) -> Path:
        assert obj in self.objects, f"Unknown object: {obj}"
        return Path(obj, obj, [])

    def compose(self, f: Path, g: Path) -> Path:
        """g after f.  Requires f.target == g.source."""
        if f.target != g.source:
            raise ValueError(
                f"Not composable: {f} has target {f.target}, "
                f"{g} has source {g.source}"
            )
        return Path(f.source, g.target, f.edges + g.edges)

    def compose_many(self, paths: list[Path]) -> Path:
        """Compose a sequence of paths left-to-right (f₁ then f₂ then ...)."""
        assert paths, "Cannot compose empty sequence"
        result = paths[0]
        for p in paths[1:]:
            result = self.compose(result, p)
        return result

    def all_paths(self, src: str, tgt: str,
                  max_length: int = 6) -> Iterator[Path]:
        """BFS over all paths from src to tgt up to max_length."""
        from collections import deque
        queue = deque([Path(src, src, [])])
        while queue:
            current = queue.popleft()
            if current.target == tgt and current.length > 0:
                yield current
            if current.length >= max_length:
                continue
            for edge_name, (esrc, etgt) in self.edges.items():
                if esrc == current.target:
                    queue.append(Path(current.source, etgt,
                                      current.edges + [edge_name]))

    def parse(self, expr: str) -> Path:
        """
        Mini text syntax:
            'f'           → single edge morphism
            'g ∘ f'       → compose: f first, then g  (standard math notation)
            'f ; g'       → compose: f first, then g  (diagrammatic order)
        """
        if '∘' in expr:
            parts = [p.strip() for p in expr.split('∘')]
            parts = list(reversed(parts))      # math notation: right to left
        elif ';' in expr:
            parts = [p.strip() for p in expr.split(';')]
        else:
            parts = [expr.strip()]

        paths = []
        for name in parts:
            src, tgt = self.edges[name]
            paths.append(Path(src, tgt, [name]))
        return self.compose_many(paths)


