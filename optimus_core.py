@dataclass
class EnrichedMorphism:
    """
    A morphism in the enriched runtime category.
    Carries: identity, type, confidence score, optional implementation,
             provenance chain, and generation counter.
    """
    name:        str
    source:      str
    target:      str
    confidence:  float = 1.0
    fn:          Optional[Callable] = None
    provenance:  list[str] = field(default_factory=list)
    generation:  int = 0          # how many refinement steps produced this
    metadata:    dict = field(default_factory=dict)

    def __call__(self, x):
        if self.fn is None:
            raise ValueError(f"Morphism '{self.name}' has no implementation")
        return self.fn(x)

    def loss(self, q: Quantale) -> float:
        return q.loss(self.confidence)

    def __repr__(self):
        gen = f" gen={self.generation}" if self.generation > 0 else ""
        return (f"{self.name} : {self.source} → {self.target} "
                f"[conf={self.confidence:.4f}{gen}]")

    def __hash__(self):
        return hash((self.source, self.target, self.name))

    def __eq__(self, other):
        return self.name == other.name


